def addition(a, b):
    """Add two objects.

    Parameters
    ----------
    a : built-in types

    b : built-in types

    Results
    -------
    sum : built-in types

    """
    return a + b


def substraction(a, b):
    """Subtract two objects.

    Parameters
    ----------
    a : built-in types

    b : built-in types

    Results
    -------
    diff : built-in types

    """

    return a - b

